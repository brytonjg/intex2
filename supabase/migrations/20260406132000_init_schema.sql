-- Initial schema for INTEX dataset (17 CSV tables)
-- Applied automatically by `supabase start` / `supabase db reset`.

begin;

-- Donor & Support domain
create table if not exists public.safehouses (
  safehouse_id integer generated by default as identity primary key,
  safehouse_code text,
  name text,
  region text,
  city text,
  province text,
  country text,
  open_date date,
  status text,
  capacity_girls integer,
  capacity_staff integer,
  current_occupancy integer,
  notes text
);

create table if not exists public.partners (
  partner_id integer generated by default as identity primary key,
  partner_name text,
  partner_type text,
  role_type text,
  contact_name text,
  email text,
  phone text,
  region text,
  status text,
  start_date date,
  end_date date,
  notes text
);

create table if not exists public.partner_assignments (
  assignment_id integer generated by default as identity primary key,
  partner_id integer not null references public.partners(partner_id) on delete restrict,
  safehouse_id integer references public.safehouses(safehouse_id) on delete restrict,
  program_area text,
  assignment_start date,
  assignment_end date,
  responsibility_notes text,
  is_primary boolean,
  status text
);
create index if not exists partner_assignments_partner_id_idx on public.partner_assignments(partner_id);
create index if not exists partner_assignments_safehouse_id_idx on public.partner_assignments(safehouse_id);

create table if not exists public.supporters (
  supporter_id integer generated by default as identity primary key,
  supporter_type text,
  display_name text,
  organization_name text,
  first_name text,
  last_name text,
  relationship_type text,
  region text,
  country text,
