  click_throughs integer,
  video_views integer,
  engagement_rate numeric,
  profile_visits integer,
  donation_referrals integer,
  estimated_donation_value_php numeric,
  follower_count_at_post integer,
  watch_time_seconds integer,
  avg_view_duration_seconds integer,
  subscriber_count_at_post integer,
  forwards integer
);

create table if not exists public.donations (
  donation_id integer generated by default as identity primary key,
  supporter_id integer references public.supporters(supporter_id) on delete set null,
  donation_type text,
  donation_date date,
  channel_source text,
  currency_code text,
  amount numeric,
  estimated_value numeric,
  impact_unit text,
  is_recurring boolean,
  campaign_name text,
  notes text,
  created_by_partner_id integer references public.partners(partner_id) on delete set null,
  referral_post_id integer references public.social_media_posts(post_id) on delete set null
);
create index if not exists donations_supporter_id_idx on public.donations(supporter_id);
create index if not exists donations_referral_post_id_idx on public.donations(referral_post_id);

create table if not exists public.donation_allocations (
  allocation_id integer generated by default as identity primary key,
  donation_id integer not null references public.donations(donation_id) on delete cascade,
  safehouse_id integer references public.safehouses(safehouse_id) on delete restrict,
  program_area text,
  amount_allocated numeric,
  allocation_date date,
  allocation_notes text
);
create index if not exists donation_allocations_donation_id_idx on public.donation_allocations(donation_id);
create index if not exists donation_allocations_safehouse_id_idx on public.donation_allocations(safehouse_id);

create table if not exists public.in_kind_donation_items (
  item_id integer generated by default as identity primary key,
  donation_id integer not null references public.donations(donation_id) on delete cascade,
  item_name text,
  item_category text,
  quantity integer,
  unit_of_measure text,
  estimated_unit_value numeric,
  intended_use text,
  received_condition text
);
create index if not exists in_kind_donation_items_donation_id_idx on public.in_kind_donation_items(donation_id);

